<p><strong>Den mer generelle gruppedagboken:</strong></p>

<h4 class="overs">Onsdag 6.oktober 2010</h4>
<p>Gruppa hadde sitt første møte der vi bestemte gruppeleder, emne, og basic ansvarsfordeling.
Gruppekjemien så ut til å stemme forholdsvis bra så forhåpentligvis kommer prosjektet til å bli ålreit å jobbe med
for alle medlemmene.</p>
<h4 class="overs"> Mandag 11. oktober 2010</h4>
<p>Andre gruppemøte, mye hadde blitt gjort siden forrige møte, Problemstillingen begynte å ta form sammtidig som gruppa delte ideer
om hvordan de andre styringsdokumentene skulle hånteres.</p>
<h4 class="overs">Onsdag 13. oktober 2010</h4>
<p>Nesten alle styringsdokumentene ligger ute i html nå. Ikke ferdige ei heller absolutte, men i utkastform. Det er bra. Virker
som om alle på gruppen synes det er bra at vi er flinke til å jobbe hjemme, men det er ytret ønske om å ta en lengre arbeidsøkt
til neste uke hjemme hos Mats. Gruppemøtene vi har er mer administrative enn produktive, og dette skal være et forsøk på å
få til en økt som blir litt av begge deler.</p>
<h4 class="overs">Mandag 25.10.2010 og veien mot 2. leveranse</h4>
<p>
Vi jobber er del med siden til den fiktive bedriften Phunsystems, men vi ser nå at vi må begynne med styringsdokumentsiden vår igjen.
Det er mye å gjøre der fram mot neste leveranse. Vi prøver og feiler, og får ting til å se presentable ut etterhvert. Alle gjør sitt
beste.
</p>
<h4 class="overs">Onsdag 27.10.2010</h4>
<p>
Startet å fokusere mer på styringsdokumentene, og kom fram til en grei fordeling av oppgaver. Arbeidsmoralen ser fortsatt ut
til å være på topp. Det blir spennende å se hva som skjer fremover.
</p>
<h4 class="overs">Mandag 01.11.2010</h4>
<p>
Vi er snart ferdig med 2. leveranse. Vi mangler en fullstendig prosjektevaluering, og vi mangler å implementere den nye
milepælsplanen.
</p>
<h4 class="overs">Mandag 08.11.2010</h4>
<p>
Vi har gått igjennom hva vi trenger å ha klart til framføringen. Dette er vel ikke akkurat noe noen av oss gleder seg spesielt til,
men vi tror det vil gå bra. 2. leveranse ble levert onsdag 3/11, og vi starter idag utviklingen av en detaljert sluttrapport. Vi tror
den kan hjelpe oss i forberedelsen til framføringen neste uke.
</p>
<h4 class="overs">Torsdag 25.11.2010</h4>
<p>
Framføringen av prosjektet vårt gikk bra, og nå skal vi benytte oss av det vi fikk tilbakemeldinger på under framføringen samt
tilbakemeldingen vi fikk på 2. leveranse.
Vi holder på å gjøre de siste forbedringene på produktet, samtidig som vi oppdaterer dagbøker, møtereferater, individuelle rapporter og
sluttrapporten. Det er mye småknot med produktet vårt som vi kunne gjort bedre. Det som vi fortsatt har tid til å få ferdig, er vi
i full gang med å utbedre nå. Dette bunner hovedsaklig i å få siden til å tilfredstille så mange WCAG 2.0-krav som mulig.
</p>
<h4 class="overs">Torsdag 14.12.2010</h4>
<p>Dette er dagen for siste møte, dagen vi leverer som gruppe for siste gang og vi går hvert til vårt.
Alle på gruppen har hatt en positiv læringskurve, og det blir litt rart å bli ferdige med samarbeidet.
Vi har klart oss gjennom prosjektet over cirka 3 måneder nå som medarbeidere og venner, og vi er svært fornøyde alle sammen med
resultatet.</p>
