<h3>Hvordan unngå/redusere sannsynligheten og risikoen for hvert punkt?</h3>

<p><strong>Ved sykdom:</strong> Meld fra til gruppa i god tid, og sørg for at alle dine dokumenter er tilgjengelig for resten av gruppa,
så arbeidsoppgavene blir utført i tide.</p>

<p><strong>Fravær og unnasluntring/ujevn fordeling:</strong> Ta opp dette på gruppemøter med den det gjelder og eventuelt gi han/henne advarsel.
Det skal tilstrebes jevn arbeidsmengde på alle fire i gruppa, og om noen føler at de har mer enn andre tas dette opp og drøftes så alle blir enige om arbeidsoppgavene.</p>

<p><strong>Uenigheter:</strong> Drøfte fordeler/ulemper ved de ulike meningene, eventuelt avtale et møte med veileder for å bli enig.</p>

<p><strong>Motivasjon:</strong> Alle på gruppa skal i størst mulig grad oppmuntre og engasjere de andre.</p>

<p><strong>Tekniske problemer:</strong> Alle må ta backup! Sørg for å bruke et filformat som alle kan bruke.
Ved manglende kunnskaper om tekniske aspekter hjelper vi hverandre så godt som mulig.</p>

<p><strong>Tidsproblemer:</strong> Omfordele arbeidsoppgaver hvis dette kan hjelpe. Omprioritering og innsnevring av deler av prosjektet kan også vurderes.</p>

<p><strong>Lite/dårlig kommunikasjon:</strong> Alle på gruppa må sjekke sin e-post så ofte som mulig, samt være tilgjengelig på telefon.
På gruppemøtene kan man ta opp med den det gjelder angående manglende tilgjengelighet eller andre kommunikasjonsproblemer.</p>

<p><strong>Bortfall av gruppemedlem:</strong> Sørge for at alle deloppgaver og gjøremål blir utført av resterende medlemmer.
Eventuelt få råd av veileder om hvordan prosjektet kan gjennomføres.</p>
