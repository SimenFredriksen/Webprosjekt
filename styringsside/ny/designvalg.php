<h2>Begrunnelse for valg av design</h2>

<h3>Behov og funksjonalitet</h3>

<p>Siden dette er en side av typen bedriftshjemmeside,
har vi tenkt over hva som er viktig å ha med på en side for nyetablert bedrift som skal ta seg inn i det norske markedet.
Vi har fokusert på at designet skal være oversiktlig og enkelt å navigere i, samt at det skal være lett å forstå "hvor man vil" på siden.
I en oppstartsfase av en bedrift er det selve informasjonen på siden som er viktigst, ikke avanserte funksjoner og andre finnesser, som veletablerte bedrifter ofte har.
</p>

<p>Vi skal ha hovedfokus på:</p>
<ul>
<li>"Behagelige" og pene kontraster og farger</li>
<li>Oversiktlig og enkelt (brukervennlighet)</li>
<li>Minst mulig overfladiske og unødvendige elementer på siden</li>
<li>Tekst som er optimalisert for web (i størst mulig grad)</li>
<li>At det skal være enkelt å forstå det som er skrevet</li>
</ul>


<h3>Layout</h3>
<p>Vi har kodet siden så hovedinnholdet på siden vil komme i midten, rett under en navigasjonsmeny.
På høyre og venstre side vil det kun være åpent (luft).
Over menyen vil firmaets logo være (header), og nederst vil det være en footer med en valideringsknapp.</p>

<p>Menyen vil inneholde de viktigste sidene (Hjem, Om oss, Support osv), så det skal være lett å navigere seg dit man vil,
uansett hvilken underside man er på. Det er også en drop-down-funksjon i kategoriene "Ansatte" og "Support" på menyen.</p>

<p>Fargevalg er per 27. oktober ikke enda bestemt.</p>


<h3>Språk</h3>
<p>Nettstedet vårt er for en nyoppstartet bedrift som vil prøve seg som spillutvikler først og fremst mot det <strong>norske</strong> spillmarkedet
(både spillinteresserte enkeltpersoner og etterhvert forhandlere), og derfor er den første websiden til bedriften på norsk (bokmål).</p>

<p>Språket skal være forstålig for alle aldersgrupper, og med minst mulig bruk av fagsjargong.
Eventuelle utrykk og forkortelser skal forklares nærmere. Dette vil bidra til at personer som ikke har drevet mye med spill før,
vil få like mye ut av siden som folk som er allerede godt kjent i spillindustrien.</p>

<p>Teksten skal i størst mulig grad være delt inn i paragrafer, med luft i mellom, så det ikke blir for tungt å lese.
Det vil også være overskrifter og underoverskrifter, så man lett forstår hva det er man egentlig leser om.</p>


<h3>Målgruppe</h3>
<p>Internasjonalt sett, ifølge blant annet Entertainment Software Association (ESA), er den gjennomsnittlige spiller 33 år i USA
(<a href="http://www.regjeringen.no/nb/dep/kkd/dok/regpubl/stmeld/2007-2008/stmeld-nr-14-2007-2008-/5/2.html?id=502868">kilde</a>).
Denne anslås å være noe lavere i Norge, men bedriften vil basere seg på at det finnes kunder i flere aldersgrupper enn bare de yngste.
Antall spillere ser ut til å falle ettersom de blir eldre.</p>

<p>Bedriften har planer om å lage spill som kan plukkes opp og spilles uten å være altoppslukende og avhengighetsskapende,
så målgruppen vil være personer i alle aldersgrupper som er spillinteresserte (i ulike grader).</p>


