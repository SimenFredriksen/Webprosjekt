<h4><a id="optimalisere"></a>Hvordan optimalisere for Web</h4>

<p>
Når vi har optimalisert teksten vår for Web har vi tenkt på den såkalte F lesingen som de fleste brukere utfører når de surfer på nettet.
Det var derfor viktig å plassere ting som var mindre viktig utenfor grensene til F formen, og det som var viktig og interessant innenfor.<p>
<p>
I tillegg har vi brukt mye avsnitt, selv der det kanskje ikke hadde vært nødvendig hvis det var snakk om en avisartikkel på papir.
Dette har vi gjort for å gjøre teksten lettere å lese, men også for å minske sjangsen for at viktige deler blir hoppet rett over. Det viktigste når vi optimalliserer for web
er at alt skal være så lett tilgjengelig som mulig, men vi vil også få brukeren til å ha lyst til å bli lenger på siden enn først antatt.

</p>

