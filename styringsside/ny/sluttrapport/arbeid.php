<h4><a id="kommunikasjon"></a>Kommunikasjon</h4>
<p>
Kommunikasjonen i gruppa har fungert bra.
Vi har møtt opp på gruppemøtene vi har avtalt, brukt e-post, lynmeldingstjenester og telefon for å kommunisere med hverandre.
Alle på gruppa har også vært tilgjengelige (på nett og/eller telefon) stort sett hele tiden, så vi har ikke hatt noen problemer med å få tak i hverandre.
</p>

<p>
Alle har også en hyggelig tone, og vi kommer godt overens med hverandre, noe som har vært viktig for samarbeidet.
</p>

<p>
I forhold til fildeling har vi brukt Google Docs og Git (github), noe som også (etterhvert) har fungert bra. Vi hadde noen problemer med
å få Git til å fungere, derfor begynte vi å dele med Google Docs for å få en god flyt i starten av prosjektet. Etterhvert forstod vi mer
av Git, men primært har vi brukt Google Docs. Noe av grunnen til dette er at Git en periode ikke ville fungere for enkelte av oss, uten at
det så ut som om vi hadde gjort noe feil. Plutselig en dag fungerte det igjen, uten at vi har noen god forklaring på dette.
</p>


<h4><a id="fordeling"></a>Fordeling</h4>
<p>
Vi har fordelt arbeidsoppgavene så vi har fått tilnærmet lik arbeidsmengde.
Alle på gruppen har vært åpne for å arbeide med det meste, så det har ikke vært noe problem å få ting gjort,
og vi er enige i at dette var en greit måte å få gjort ting på.
Vi har også gjort en del ting sammen, som f.eks. design, prosjektbeskrivelse, møtereferater m.m.
</p>
<p>
Selv om vi har hatt egne ansvarsområder, har vi hjulpet hverandre og sett over arbeidet de andre har utført så alle får sagt hva de mener.
Det har fungert veldig bra med denne måten, og alle har gjort det de skal i tide.
Prosjektmøtene har blitt brukt til (blant annet) å se over hva alle har gjort, og satt ting sammen.
</p>
<p>
En grov oversikt over fordelingen kan ses på <a href="../ansvarskart.php">ansvarskartet</a> og <a href="../aktivitetsplan.php">aktivitetsplanen</a>.
</p>

