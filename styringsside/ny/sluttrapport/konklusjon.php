<h4><a id="maaloppnaaelse"></a>M&aring;loppn&aring;else</h4>
<p>
Ved prosjektstart skrev vi ned en målsetting som kan oppsummeres slik:
</p>
<ul>
	<li>Lage en webside for en bedrift.</li>
	<li>Lære å sette opp en webside, ved blant annet:
		<ul>
			<li>Hvordan optimalisere for web.</li>
			<li>Standardisere i henhold til W3C-standardene.</li>
			<li>Kildekritikk</li>
		</ul>
	</li>
	<li>Prøve å formidle informasjon via web på best mulig måte.</li>
	<li>Skape reklame på bedriftens vegne basert på uferdig arbeid.</li>
	<li>Oppleve  hvordan det er å jobbe i prosjekt med ukjente mennesker.</li>
	<li>Lære om prosjektdokumentasjon.</li>
</ul>
<p>
Vi diskuterte hva slags webside vi skulle lage <a href="../motereferat.php#ref1">første gangen vi satt på gruppemøte</a>. Vi kom
etterhvert fram til at vi skulle lage en webside for bedrift, og har også i ettertid brukt mye tid på gruppemøter. Vi har hatt
4 timer "obligatorisk" oppmøte hver uke i tillegg til de prosjektrelaterte oppgavene som ble relativt jevnt fordelt på alle
medlemmene. Bruker relativt her fordi det ikke er sånn at vi har gjort nøyaktig 1/4 hver, men det er allikevel jevnt nok til
at ingen har gjort mye mer enn andre på gruppen.
</p>
<p>
Når vi satte i gang arbeidet på dette prosjektet, hadde ingen av oss erfaring med HTML 5, XHTML eller CSS 2. Noen av oss hadde erfaring
med HTML til hobbybruk og private websider, men ingen av oss hadde brukt CSS før. HTML bruken tidligere har vært av den ikke veldig
populære sorten (har vi nå oppfattet), hvor vi både definerer innhold og utseende (layout) uten stilark. Derfor gikk det med en del tid
bare på å forstå CSS, og på å få siden til å se ut slik vi ville at den skulle se ut. Ved å over tid tilegne oss den nødvendige den
nødvendige kompetansen, har vi utviklet en side som er vårt prosjeks produkt. Kildekritikk knyttet til
utviklingen av produktet har ikke vært viktig, siden vi måtte finne på informasjonen på produktet vårt selv fordi
spillbedriften er fiktiv. Dette er viktigere i andre prosjekter for eksempel hvis man lager en informasjonskanal til et bestemt tema.
</p>
<p>
Når det gjelder styringsdokumentsiden, er det viktigst å gjøre dokumentasjonen lett leselig og at den er enkel å finne. Dette innebærer at
bakgrunnsfarge og tekstfarge ikke går inn i hverandre, og at linkene er laget for enklest mulig navigering
(smart navigering). Dette er forøvrig kjempeviktig for alle websider man lager.
For vår produkts og vår <a href="http://www.stud.hio.no/~s168943/prosjekt2010/hovedside/index.html">hovedsides</a> del
har vi altså tatt for oss en nyetablert bedrift, og ut ifra det måtte vi tenke på hva besøkende vil være interessert i. Vi kom fram
til at kunder og besøkende på spillutvilkeres websider ofte vil vite mer om bakgrunnen til selskapene. Dette har vi skjønt etter
å ha besøkt etablerte spillutviklere og indie-spillutviklere på world wide web. Der er det alltid en slags
<a href="http://www.stud.hio.no/~s168943/prosjekt2010/hovedside/faq.html">FAQ</a> (ofte stilte spørsmål),
en kontaktside og flere sider rundt produktene/prosjektene deres. De sidene som derimot ikke har noe med verken bakgrunnen til bedriften eller
produktene/prosjektene å gjøre, inneholder ofte lite informasjon og er åpenbart ikke ikke hovedfokuset. Siden vår bedrift var
nyoppstartet, prøvde vi å skrive inn spennende ting som kan
virke som reklame for bedriftens første produkt. Alt fra personalets tidligere prosjekter til personalets personlige spillpreferanser
kan virke som reklame, for slike ting kan vitne om god smak. Dette kan selvsagt også virke motsatt vei,
derfor er det greit at vi har med annen informasjon på <a href="http://www.stud.hio.no/~s168943/prosjekt2010/hovedside/faq.html">FAQ</a>'en
og under linken "<a href="http://www.stud.hio.no/~s168943/prosjekt2010/hovedside/omoss.html">Om oss</a>".
</p>
<p>
Det å jobbe med ukjente mennesker i prosjekt kan være vanskelig. Heldigvis for oss har vi kun positive opplevelser med den
gruppen vi ble satt i. Ting ble gjort, fristene ble overholdt, gruppemøtene gikk bra og det var alltid en hyggelig tone som la et meget
godt grunnlag for prossessen vi skulle igjennom.
</p>
<p>
Tidsforbruket av prosjektet vårt er nok delt inn i flere deler, men 2 av disse er større enn andre. Den ene delen er knyttet til CSS,
og den andre er knyttet til styringsdokumentene. Vi har lært mye innenfor det å dokumentere arbeid til prosjekt, og dette er helt
klart et sted hvor vi har forbedringspotensiale. Burde åpenbart brukt mye mer tid på de viktige planene som skal leveres i første
leveranse.
</p>
<h4><a id="evaluering"></a>Evaluering av sluttproduktet</h4>
<p>
Vi har hatt flere krav til sluttproduktet vårt, blant annet at siden skal validere i HTML 5 og CSS 2. I tillegg skal siden være
universiell utformet, og vi har passet på at fargene har verdier som gir god kontrast og lysstryke. Et eksempel er
<a href="http://www.accesskeys.org/tools/color-contrast.html">menyen</a>
på hovedsiden vår som har fargene svart (#000000) og gulaktig (#fddd7c) som gir fargeforskjell på nesten 600 og lysstyrke på 220.
Vi har brukt flere forskjellige verktøy for å få det hele til å stemme, og vi er godt fornøyde med det vi har fått til.
</p>
<h4><a id="oppsummering"></a>Oppsummering</h4>
<p>
Samarbeidet vårt har gått meget bra, og miljøet i gruppen har vært over all forventning. Å sette 4 forskjellige mennesker som er
ukjente for hverandre til å jobbe sammen kan være både tungt og vanskelig, men her har alle prøvd å gjøre sitt beste. Vi valgte
på første gruppemøte å ha en leder i stedet for en flat struktur, men det viste seg at vi kunne klart oss godt både med og uten
gruppeleder. Dette er ikke ment negativt i forhold til hvem som ble leder. Gruppen vår var flinke til å gi seg selv oppgaver
og diskutere ting fram til enighet, og lederen var aldri nødt til å skjære igjennom å ta beslutninger på vegne av gruppen. Samtidig
gir det absolutt en trygghet å ha en gruppeleder i tilfelle vi hadde bruk for en, noe alle på gruppen er enige om.
</p>
<p>
Vi har laget en webside som alle på gruppen stiller seg bak, og er meget godt fornøyde med. Siden er oversiktlig og linkene er
logiske. Du kan gå fra en hvilken som helst side, til en hvilken som helst annen side. De sidene vi har opprettet mener vi er viktige
å ha for en spillbedrift, og flere av disse er generelt viktige for alle bedrifter. Vi har lært mye om faget i denne prosjektprossessen,
rundt både det å lage websider og det å dokumentere prosjekter.
</p>
<p>
Samtidig ser vi flere forbedringspotensial i produktet vårt, som vi kunne utviklet hvis vi hadde bedre tid.
</p>
<h4><a id="sluttord"></a>Sluttord og tanker om videre utvikling av prosjektet</h4>
<p>
Vi kunne nok sett at siden vår hadde mer informasjon enn det den har. Det er mange aspekter rundt spillindustri vi kunne gått
dypere inn i, og det er flere ting vi ikke tar for oss. Vi tar ikke med maskinvarekrav for å få spillet til å fungere, eller fungere
optimalt. Vi kunne også tenke oss å legge inn flere prosjekter, og nærmere innblikk i personalets historie.
</p>
<p>
PHP er også noe vi kunne tenkt å implementere i produktet vårt for å gjøre sidene mindre statiske. SQL hadde også kunne hjulpet websiden
slik at vi kunne utviklet et nettsamfunn (forum) hvor bedriften og forbrukerne kunne omgå hverandre. Dette har vi sett er populært
hos de veletablerte spillutviklerne. Grunnet liten kompetanse innen SQL og for liten tid, så vi oss nødt til å droppe dette. Men det
er en helt opplagt måte å forbedre produktet vårt på.
</p>
<p>
Når det gjelder layout så kunne vi tenkt oss flere bilder og en mer spennende hovedbakgrunn. I hovedsak for å gjøre siden mer fargerik
og spennende. Vi valgte å droppe dette fordi en lav oppløsning ville ikke vist eventuelle bilder utenfor informasjonsboksen. I tillegg
strakk ikke tiden til for å teste dette fullt ut.
</p>
