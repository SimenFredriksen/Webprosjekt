<h4><a id="bakgrunn"></a>Bakgrunn for prosjektet</h4>
<p>Vi møttes første dagen som fire ukjente, som ingen
hadde vært borti hverken html eller css før denne høsten. Det gikk litt stille for seg på første møte, men tok seg opp ettersom
vi ble litt bedre kjent. Alle kom godt overens og folk holdt avtaler og gjorde sin innsats i prosjektet.

Det var mye diskusjon om hvilke av de ulike oppgavene vi skulle velge. De fleste ville i utgangspunktet å lage en informasjonskanal,
f.eks. for musikk eller tv-serier. Men det føltes som en nokså unyttig bruk av tid og kunnskaper, og føltes litt for «barnslig».
Vi valgte istedet å lage en webside for en bedrift.

Det stod mellom to valg; enten å lage en nettside til en etablert bedrift, eller om å «starte» vår egen bedrift, og lage nettside
til den. Konklusjonen var at det ble muligens for vanskelig å lage noe originalt til en bedrift som allerede har en hjemmeside, så
vi valgte å gå med forslag nummer to. Problemstillingen vår ble da: «Hvordan bør en nyetablert bedrifts webside se ut?»</p>
<h4><a id="hensikt"></a>Hensikt</h4>
<p>Vi hadde lyst til å prøve oss på å lage en side til en fiktiv bedrift, for å skaffe oss
et inntrykk av hvordan det er.Vi tenkte at dette kunne være nyttig å ha prøvd seg på fordi det kan bli aktuelt senere i arbeidslivet.
</p>
<h4><a id="tema"></a>Tema</h4>
<p>Vi surfet en del på nettet etter ideer til hva vi ville jobbe med og kom over flere nettsteder
for spillutviklere. Det som skilte seg ut her var at vi trengte ikke nødvendigvis ha et produkt å «selge», men likevel kunne opprette
et nettsted som hadde prosjekter under utvikling. Vi valgte da å lage bedriftwebside til en fiktiv bedrift kalt «Phunsystems», som har
som mål å utvikle action plattformspill for voksne. Flere av gruppemedlemmene er spillinteresserte og alle var enige om at dette var
et morsomt og interessant tema, som i tillegg var «data relevant».</p>
